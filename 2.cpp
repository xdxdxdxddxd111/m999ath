#include <iostream>  // Подключаем библиотеку для ввода‑вывода (cout, endl)
#include <iomanip> // Подключаем библиотеку для форматирования вывода (setprecision)
#include <cmath>   // Подключаем математическую библиотеку (cos, pow, fabs)
using namespace std; // Используем стандартное пространство имён (не нужно писать std::)

// Функция f(x) = cos(x) − x³ + 2
double f2(double x) {
    return cos(x) - pow(x, 3) + 2; // Возвращает значение функции в точке x
}

// Метод дихотомии: ищет корень на отрезке [a, b] с точностью eps
double dichotomy2(double a, double b, double eps) {
    double fa = f2(a), fb = f2(b); // Значения функции на концах отрезка

    if (fa * fb > 0) { // Если знаки f(a) и f(b) одинаковые — корня нет
        cout << "На отрезке [" << a << ", " << b << "] нет корня" << endl;
        return NAN; // Возвращаем «не число»
    }

    int iteration = 0; // Счётчик итераций

    while (fabs(b - a) > eps) { // Повторять цикл, пока длина текущего отрезка поиска корня больше, чем заданная точность eps
        iteration++; // Увеличиваем счётчик итераций
        double c = (a + b) / 2; // Середина отрезка
        double fc = f2(c); // Значение функции в середине отрезка

        if (fabs(fc) < eps) return c; // Если f(c) ≈ 0 — нашли корень

        if (fa * fc < 0) {
            b = c; // Корень в левой половине — сужаем правую границу
            fb = fc; // Обновляем значение функции на правой границе
        } else {
            a = c; // Корень в правой половине — сужаем левую границу
            fa = fc; // Обновляем значение функции на левой границе
        }
    }

    cout << "Задача 2 (дихотомия): найдено за " << iteration << " итераций" << endl;
    return (a + b) / 2; // Возвращаем середину последнего отрезка как корень
}

// Метод итераций: ищет корень с начального приближения x0 и точностью eps
double iteration_method(double x0, double eps) {
    double x = x0; // Текущее приближение корня
    int iteration = 0; // Счётчик итераций

    do {
        iteration++; // Увеличиваем счётчик итераций
        double x_next = cos(x) - pow(x, 3) + 2 + x; // Следующее приближение

        if (fabs(x_next - x) < eps) break; // Если точность достигнута — выходим
        x = x_next; // Обновляем текущее приближение
    } while (iteration < 1000); // Максимум 1000 итераций (защита от зацикливания)

    cout << "Задача 2 (итерации): найдено за " << iteration << " итераций" << endl;
    return x; // Возвращаем найденный корень
}

int main() {
    cout << fixed << setprecision(6); // Фиксированная точка, 6 знаков после запятой

    cout << "ЗАДАЧА 2" << endl;
    cout << "Локализация корней: f2(x) = cos(x) - x^3 + 2" << endl;
    cout << "Интервал с корнем: [1, 2]" << endl << endl;

    // Поиск корня методом дихотомии (точность 0.01)
    double root2_dichotomy = dichotomy2(1, 2, 0.01);
    cout << "Корень методом дихотомии (точность 0.01): " << root2_dichotomy << endl;

    // Поиск корня методом итераций (точность 0.001)
    double root2_iteration = iteration_method(1.5, 0.001);
    cout << "Корень методом итераций (точность 0.001): " << root2_iteration << endl;

    return 0; // Успешное завершение программы
}
