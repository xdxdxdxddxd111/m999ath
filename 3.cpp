#include <iostream>
#include <iomanip>
#include <cmath>
using namespace std;

const int N = 4;          // Количество уравнений и неизвестных
const double EPS = 0.001; // Требуемая точность


int main() {
    // Матрица коэффициентов (правая часть после знака равенства, без свободных членов)
    double A[N][N] = {
        {0.15,  0.05, -0.08,  0.16},
        {0.32, -0.42, -0.13,  0.11},
        {0.17,  0.06, -0.08,  0.12},
        {0.21, -0.16,  0.39,  0.00}
    };

    // Вектор свободных членов (константы в конце каждого уравнения)
    double b[N] = {-0.47, 1.24, 1.18, -0.84};

    double x[N] = {0};      // Текущее приближение (начальное — нули)
    double x_prev[N] = {0};  // Предыдущее приближение
    int iteration = 0;       // Счётчик итераций
    double max_diff;          // Максимальная разница между итерациями

    cout << fixed << setprecision(4); 
    cout << "Решение методом простой итерации (точность " << EPS << "):\n\n";

    do {
        iteration++;
        max_diff = 0; // обнуляет переменную перед началом новой итерации цикла

        // Сохраняем предыдущее приближение
        for (int i = 0; i < N; i++) {  // цикл for последовательно копирует все элементы текущего решения x в массив x_prev, чтобы на следующей стадии итерации использовать старые значения для расчёта новых и оценки прогресса
            x_prev[i] = x[i];
        }

        // Вычисляем новое приближение для каждого x[i]
        for (int i = 0; i < N; i++) { // Внешний цикл: перебираем все уравнения системы (i = 0, 1, 2, 3) — для каждого вычисляем новое значение x[i]
            x[i] = b[i];  // Инициализируем x[i] значением свободного члена из i‑го уравнения (берём константу из правой части уравнения)
            for (int j = 0; j < N; j++) { // Внутренний цикл: перебираем все неизвестные (j = 0, 1, 2, 3), чтобы учесть их вклад в x[i]
                x[i] += A[i][j] * x_prev[j];  // Обновляем x[i]: прибавляем произведение коэффициента A[i][j] (из матрицы) на предыдущее значение x_prev[j] (с прошлой итерации)
            }
        }

        // Находим максимальную разницу между приближениями
        for (int i = 0; i < N; i++) { // Внешний цикл: перебираем все неизвестные (x₁, x₂, x₃, x₄) — для каждого вычисляем разницу между текущим и предыдущим приближением
            double diff = fabs(x[i] - x_prev[i]); // Вычисляем абсолютную разницу (модуль разности) между текущим значением x[i] и его предыдущим значением x_prev[i]
            if (diff > max_diff) max_diff = diff; // Сравниваем полученную разницу с текущей максимальной (max_diff); если новая разница больше, обновляем max_diff — теперь она хранит наибольшее отклонение среди всех неизвестных
        }

    } while (max_diff > EPS && iteration < 1000);  // Останов при достижении точности или 1000 итерациях

    // Вывод 
    cout << "Решение найдено за " << iteration << " итераций:\n";
    for (int i = 0; i < N; i++) {
        cout << "x" << (i + 1) << " = " << x[i] << "\n";
    }

    return 0;
}
