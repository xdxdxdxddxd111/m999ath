#include <iostream>  // Библиотека ввода‑вывода
#include <iomanip>  // Для setprecision (точность вывода)
#include <cmath>   // Для fabs (модуль) и max
using namespace std; // Стандартное пространство имён

const int N = 3;    // Число уравнений/неизвестных
const double EPS = 0.001; // Точность вычислений

int main() {
    // Матрица коэффициентов
    double A[N][N] = {{3.3, 3.7, 4.2}, {2.7, 2.3, -2.9}, {4.1, 4.8, -5.1}};
    
    // Вектор свободных членов
    double b[N] = {5.8, 6.1, 6.9};
    
    double x[N] = {0};  // Приближения (начальные — нули)
    int iter = 0;       // Счётчик итераций
    
    cout << fixed << setprecision(4);  // Формат: 4 знака после запятой
    
    do {  // Цикл итераций
        double max_diff = 0, prev;  // Макс. разница и предыдущее значение x[i]
        iter++;  // Увеличиваем счётчик итераций
        
        for (int i = 0; i < N; i++) {  // По всем уравнениям (i = 0, 1, 2)
            prev = x[i];  // Сохраняем старое значение x[i]
            x[i] = b[i];  // Начинаем с свободного члена
            
            for (int j = 0; j < N; j++) {  // По всем неизвестным (j = 0, 1, 2)
                if (j != i) x[i] -= A[i][j] * x[j];  // Вычитаем вклад x[j] (с учётом обновлённых значений)
            }
            x[i] /= A[i][i];  // Делим на диагональный коэффициент
            max_diff = max(max_diff, fabs(x[i] - prev));  // Обновляем макс. разницу
        }
    } while (max_diff > EPS && iter < 1000);  // Пока не достигнута точность или не превышен лимит итераций (1000)
    
    cout << "Решение найдено за " << iter << " итераций:\n";  // Вывод количества итераций
    for (int i = 0; i < N; i++) cout << "x" << i+1 << " = " << x[i] << "\n";  // Вывод результатов (x₁, x₂, x₃)
    
    return 0; 
}
